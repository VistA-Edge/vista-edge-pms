private void getReport(){
    try{
        AZSlidingStatement az=new AZSlidingStatement();

        if(radioStms.isSelected()){
            String claimNo=cmbClaimID.getSelectedIndex()==-1?"":
                           cmbClaimID.getSelectedItem().toString();
            az.generateAZSlidingStms(claimNo);
        }

        if(radioSum.isSelected()){
             az.generateAZSlidingSum(
             Utiles.convertUSDatetoMysql(sdate.getSelectedDate()),
             Utiles.convertUSDatetoMysql(edate.getSelectedDate()));
        }
    }catch(Exception e){e.printStackTrace();}
}

